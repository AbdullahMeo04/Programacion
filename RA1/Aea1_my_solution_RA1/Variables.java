public class Variables {
 	public static void main(String[] args) {
 		int a = 5;
 		boolean b = true;
 		double c = 9.99;
 		char d = 'a';
 		String e = "barcelona";

 		System.out.println(a);
 		System.out.println(b);
 		System.out.println(c);
 		System.out.println(d);
 		System.out.println(e);
 	}
 }